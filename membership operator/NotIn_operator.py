# Example to demonstrate the 'not in' operator

# Define a list
fruits = ["apple", "banana", "cherry"]
# Check if a value is not in the list
fruit_to_check = "orange"
if fruit_to_check not in fruits:
    print(f"{fruit_to_check} is not in the list.")
else:
    print(f"{fruit_to_check} is in the list.")
