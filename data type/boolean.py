# Introduction to Boolean Data Type

# Boolean values: True or False
is_raining = True
is_sunny = False

print("Boolean Data Type Example")
print("Is it raining?", is_raining)
print("Is it sunny?", is_sunny)

# Boolean expressions
print("5 > 3:", 5 > 3)  # True
print("10 == 20:", 10 == 20)  # False

